def addition(num1, num2)
  answer = num1.to_i + num2.to_i
  return answer
end

def subtraction(num1, num2)
  answer = num1.to_i - num2.to_i
  return answer
end

def division(num1, num2)
  answer = num1.to_i / num2.to_i
  return answer
end

def multiplication(num1, num2)
  answer = num1.to_i * num2.to_i
  return answer
end

def modulo(num1, num2)
  answer = num1.to_i % num2.to_i
  return answer
end

def square_root(num)
  answer = Integer.sqrt(num)
  return answer
end